package org.example;

public class JavaDataTypes {
    public static void main(String[] args) {
        // Declare and initialize variables with different data types
        byte byteValue = 100;
        short shortValue = 1000;
        long longValue = 1000000000L;
        float floatValue = 1234567.89f;
        double doubleValue = 123456.78;
        char charValue = 'A';
        boolean booleanValue = true;

        // Print out all 6 variables that are all different data types on your console
        System.out.println(byteValue);
        System.out.println(shortValue);
        System.out.println(longValue);
        System.out.println(floatValue);
        System.out.println(doubleValue);
        System.out.println(charValue);
        System.out.println(booleanValue);
    }
}
