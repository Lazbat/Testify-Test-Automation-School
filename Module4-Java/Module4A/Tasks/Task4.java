package org.Tasks;

public class Task4 {
    public static void main(String[] args) {
        //find the area of a circle with radius of 12.7m
        double radius = 12.7;

        //The formula for the area of a circle is:
        //Area = π * radius * radius
        //π is a mathematical constant 3.14159

        //Calculate area of circle
        double area = 3.14 * radius * radius;

        //Print out your result with the unit of measurement which is metres
        System.out.println("The area of the circle with radius " + radius + " meters = " + area + " square meters.");

    }
}
