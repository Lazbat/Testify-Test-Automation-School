package task14;

public class ClassA {
    private int shapeSides = 4;  //number of sides of the shape
    private double shapeLength;  //length of the shape
    private double shapeBreadth;  //breadth of the shape

    public int getShapeSides() {
        return shapeSides;
    }

    public double getShapeLength() {
        return shapeLength;
    }

    public void setShapeLength(double shapeLength) {
        this.shapeLength = shapeLength;
    }

    public double getShapeBreadth() {
        return shapeBreadth;
    }

    public void setShapeBreadth(double shapeBreadth) {
        this.shapeBreadth = shapeBreadth;
    }
}
