//the 'reverse()' function is used to reverse elements in an array
function reverseArray(array) {
    return array.reverse();
}
const myArray = [14,78,9,0,56,8];
const reversedArray = reverseArray(myArray);
console.log("Reversed Array:", reversedArray)

function reverseArray1(array) {
    return array.reverse();
}
const myArray1 = ['Python', 'Java', 'JavaScript']
const reversedArray1 = reverseArray1(myArray1);
console.log("Reversed Array1:", reversedArray1)